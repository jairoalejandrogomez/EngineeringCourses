%--------------------------------------------------------------------------
% Desarrolle una función en Matlab que le aplique la transformada inversa discreta de Fourier en 2D a una
% matriz de entrada. 
%--------------------------------------------------------------------------
%I:    Imagen de entrada
%T:    2D DFT de la imagen de entrada. 
%--------------------------------------------------------------------------
function T=IDFT_2D(I)
%--------------------------------------------------------------------------

I1=double(I);

[M,N]=size(I1);


for x=0:M-1
    
    for y=0:N-1
        
        %------------------------------------------------------------------
        sumatoria=0;
        
        for u=0:M-1
            
            for v=0:N-1
                
                sumatoria=sumatoria+I1(u+1,v+1)*exp(j*2*pi*((u*x/M)+(v*y/N)));
                
            end
            
        end
        
        T(x+1,y+1)=(1/(M*N))*sumatoria;
        %------------------------------------------------------------------
        
    end
    
end

%--------------------------------------------------------------------------